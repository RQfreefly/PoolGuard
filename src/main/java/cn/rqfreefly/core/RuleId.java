package cn.rqfreefly.core;

/**
 * 风险检测规则标识。
 */
public enum RuleId {
    /** 高频入口创建线程池，可能导致重复创建与资源膨胀。 */
    PG001,
    /** 未限制任务提交或消费速率，可能触发任务堆积。 */
    PG002,
    /** 线程池缺少明确关闭路径，存在生命周期泄露风险。 */
    PG003,
    /** 线程池阻塞等待策略不当，可能导致调用链长期占用。 */
    PG005,
    /** 使用默认线程工厂，线程命名和异常处理能力不足。 */
    PG006,
    /** 使用无界队列承载任务，存在内存持续增长风险。 */
    PG007,
    /** 定时线程池任务长期运行且未回收，可能导致资源泄露。 */
    PG008,
    /** 线程池配置不合理导致拒绝策略或容量设置风险。 */
    PG009
}
